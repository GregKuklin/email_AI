def get_country_iso_code(user_input: str) -> str | None:
    """
    Принимает пользовательский ввод, нормализует его и возвращает ISO код страны.

    Args:
        user_input: Название страны от пользователя (например, "В Турцию").

    Returns:
        ISO 3166-1 alpha-2 код страны или None, если страна не найдена.
    """
    normalized_input = user_input.strip().lower()

    # Сначала проверяем вариации (падежи, предлоги)
    if normalized_input in COUNTRY_VARIATIONS:
        normalized_input = COUNTRY_VARIATIONS[normalized_input]
    
    # Затем ищем ISO код по нормализованному названию
    return COUNTRY_TO_ISO.get(normalized_input)

