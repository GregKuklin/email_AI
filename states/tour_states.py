"""
FSM состояния для работы с поиском туров.
Определяет все возможные состояния пользователя в процессе подбора тура.
"""

from aiogram.fsm.state import State, StatesGroup


class TourSearchFlow(StatesGroup):
    """
    Состояния для полного flow поиска туров.
    Поддерживает два сценария: ручной ввод параметров и свободное описание.
    """
    
    # ============================================
    # Выбор сценария
    # ============================================
    select_scenario = State()  # Выбор между "Ввести параметры" и "Свободное описание"
    
    # ============================================
    # Сценарий 1: Ввод параметров вручную
    # ============================================
    waiting_departure_city = State()      # Город вылета (Moscow, SPb, Other)
    waiting_custom_city = State()         # Ожидание ввода города на русском
    waiting_country = State()             # Страна назначения (TR, EG, etc.)
    waiting_custom_country = State()      # Ожидание ввода страны на русском
    waiting_destination_city = State()    # Город назначения (Kemer, Antalya, etc.)
    waiting_custom_destination = State()  # Ожидание ручного ввода города назначения
    waiting_dates = State()               # Даты поездки
    waiting_nights = State()              # Количество ночей (7..9, 10..14)
    waiting_adults = State()              # Количество взрослых
    waiting_kids = State()                # Количество детей
    waiting_kids_ages = State()           # Возраста детей (если kids > 0)
    waiting_budget = State()              # Бюджет (опционально)
    waiting_stars = State()               # Минимальное количество звезд (опционально)
    waiting_amenities = State()           # Выбор удобств (pool, spa, wifi, etc.)
    confirm_params = State()              # Подтверждение всех параметров
    
    # ============================================
    # Сценарий 2: Свободное описание
    # ============================================
    waiting_free_text = State()           # Ожидание свободного текста от пользователя
    clarifying_params = State()           # Уточнение недостающих параметров
    ai_parsing = State()                  # Парсинг текста через ChatGPT
    ai_confirm_params = State()           # Подтверждение распознанных параметров
    
    # ============================================
    # Поиск и просмотр результатов
    # ============================================
    searching_tours = State()             # Поиск выполняется (показываем индикатор)
    browsing_tours = State()              # Просмотр ленты туров (бесконечная лента)
    viewing_tour_details = State()        # Просмотр деталей конкретного тура
    viewing_hotel_rooms = State()         # Просмотр номеров в отеле
    asking_hotel_question = State()       # Ожидание вопроса о текущем отеле
    
    # ============================================
    # Дополнительные действия
    # ============================================
    requesting_pdf = State()              # Генерация PDF
    changing_criteria = State()           # Изменение критериев поиска
    
    # ============================================
    # Выборочное редактирование параметров
    # ============================================
    editing_params = State()              # Меню редактирования параметров
    editing_single_param = State()        # Редактирование одного параметра
    ai_editing_context = State()          # AI редактирование с контекстом


class InlineStates(StatesGroup):
    """
    Состояния для inline взаимодействий, которые не требуют полного FSM flow.
    Используется для быстрых действий типа "Получить вдохновение".
    """
    
    inspiration_category = State()        # Выбор категории вдохновения
    chatting_with_navia = State()         # Общение с AI ассистентом Navia
